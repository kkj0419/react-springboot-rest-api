package com.example.gccoffe.service;

import com.example.gccoffe.model.Category;
import com.example.gccoffe.model.Product;
import com.example.gccoffe.repository.ProductRepository;
import org.springframework.stereotype.Service;

import java.time.LocalDateTime;
import java.util.List;
import java.util.UUID;

@Service
public class DefaultProductService implements ProductService {

    private final ProductRepository productRepository;

    public DefaultProductService(ProductRepository productRepository) {
        this.productRepository = productRepository;
    }

    @Override
    public List<Product> getProductsByCategory(Category category) {
        return productRepository.findByCategory(category);
    }

    @Override
    public List<Product> getAllProducts() {
        return productRepository.findAll();
    }

    @Override
    public Product createdProduct(String productName, Category category, long price) {
        Product newProduct = new Product(UUID.randomUUID(), productName, category, price);
        return productRepository.insert(newProduct);
    }

    @Override
    public Product createdProduct(String productName, Category category, long price, String description) {
        Product newProduct = new Product(UUID.randomUUID(), productName, category, price, description, LocalDateTime.now(),LocalDateTime.now());
        return productRepository.insert(newProduct);
    }
}
